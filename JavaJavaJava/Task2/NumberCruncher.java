package JavaJavaJava.Task2;

public class NumberCruncher {
    private int num;

    NumberCruncher(int n) {
        num = n;
    }

    public int getNum() {
        return num;
    }

    public int doubleNum() {
        return 2*num;
    }

    public int tripleNum() {
        return 3*num;
    }

    public int squareNum() {
        return num*num;
    }
}
