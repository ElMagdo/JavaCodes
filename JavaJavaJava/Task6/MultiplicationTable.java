public class MultiplicationTable {
    public static void uptoSquare(int n) {
        for (int i = 1; i <= n; i++)
            System.out.print(i * n + " ");
    }

    public static void table(int l) {
        for (int i = 1; i <= l; i++)
            System.out.print(" " + i);

        System.out.println();

        
    }
}
